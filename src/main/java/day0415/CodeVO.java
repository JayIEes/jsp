package day0415;

public class CodeVO {
	private int code;
	private String codeText;
	
	
	
	public CodeVO() {
	}



	public CodeVO(int code, String codeText) {
		super();
		this.code = code;
		this.codeText = codeText;
	}



	/**
	 * @return the code
	 */
	public int getCode() {
		return code;
	}



	/**
	 * @param code the code to set
	 */
	public void setCode(int code) {
		this.code = code;
	}



	/**
	 * @return the codeText
	 */
	public String getCodeText() {
		return codeText;
	}



	/**
	 * @param codeText the codeText to set
	 */
	public void setCodeText(String codeText) {
		this.codeText = codeText;
	}



	
	
}
